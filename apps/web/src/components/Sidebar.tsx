const iconMap = {
  dashboard: DashboardIcon,
  specs: SpecsIcon,
  extension: ExtensionIcon,
  settings: SettingsIcon,
};

export function Sidebar({ role, currentPath }: SidebarProps) {
  const navItems = getNavItems(role);
  const roleLabel = role === "requestor" ? "REQUESTOR" : "WORKER";

  return (
    <aside className="w-[220px] bg-[var(--surface)] border-r border-[var(--border)] flex flex-col h-full">
      {/* Role Label */}
      <div className="px-4 py-4">
        <span className="text-[var(--text-muted)] text-xs font-medium tracking-wider">
          {roleLabel}
        </span>
      </div>

      {/* Nav Items */}
      <nav className="flex-1 px-3">
        {navItems.map((item) => {
          const isActive = isNavItemActive(item.href, currentPath);
          const Icon = iconMap[item.icon];

          return (
            <a
              key={item.href}
              href={item.href}
              className={`
                flex items-center gap-3 px-3 py-2.5 rounded-md mb-1
                text-sm font-medium transition-colors duration-150
                ${
                  isActive
                    ? "bg-[var(--surface-2)] text-[var(--accent)]"
                    : "text-[var(--text-secondary)] hover:text-[var(--text-primary)] hover:bg-[var(--surface-2)]"
                }
              `}
            >
              <Icon className="w-5 h-5" />
              {item.label}
            </a>
          );
        })}
      </nav>

      {/* Extension Status - Worker only */}
      {role === "worker" && <ExtensionStatus variant="sidebar" />}
    </aside>
  );
}
